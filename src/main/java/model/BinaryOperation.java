package model;

/**
 * Enum represents binary operations and symbols for their showing to user
 *
 * @author Yaroslav Piskachov
 */
public enum BinaryOperation implements Operation{
    /**
     * Plus operation
     */
    PLUS,
    /**
     * Minus operation
     */
    MINUS,
    /**
     * Multiply operation
     */
    MULTIPLY,
    /**
     * Divide operation
     */
    DIVIDE;
}